package com.material.website.system;

/**  
 * @Description: 日志操作类型 
 * @author 张明虎 zhangminghu@yuntengzhiyong.com  
 * @date 2014年12月15日 下午7:49:14  
 */

public enum OperateType {
	/**
	 * 日志操作类型0为添加1为移除2为更新3为登录4为退出5为评论6为打分7为审核8为反馈
	 */
	ADD("添加"),REMOVE("移除"),UPDATE("更新"),LOGIN("登录"),LOGOUT("退出")
	,ADD_COMMENT("评论"),ADD_SCORE("打分"),AUDIT("审核"),FEED_BACK("反馈"),RENEW("恢复");
	private String name;
	/**
	 * 构造函数
	 * @param name
	 */
	private OperateType(String name) {
		this.name = name;
	}
	/* (non-Javadoc)
	 * @see java.lang.Enum#toString()
	 */
	@Override
	public String toString() {
		return name;
	}
}
