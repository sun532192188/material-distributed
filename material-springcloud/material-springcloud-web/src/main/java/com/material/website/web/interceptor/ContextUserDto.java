package com.material.website.web.interceptor;

import com.material.website.entity.Admin;



/**
 * 上下文存放的用户属性，存用户对象
 * @author winston.wang
 */
public class ContextUserDto {

    private Admin user;

    public ContextUserDto(Admin user) {
        this.user = user;
    }

    public Admin getUser() {
        return user;
    }

    @Override
    public String toString() {
        return "ContextUserDto{" +
                "user=" + user +
                '}';
    }
}
