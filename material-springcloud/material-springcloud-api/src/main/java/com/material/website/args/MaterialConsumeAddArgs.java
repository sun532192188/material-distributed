package com.material.website.args;

import java.util.Date;

/**
 * 物资消耗(出库/调拨)参数类
 * 
 * @author sunxiaorong
 *
 */
public class MaterialConsumeAddArgs {
	/**
	 * 主键
	 */
	private Integer id;
	/**
	 * 操作编号
	 */
	private String operatNo;
	/**
	 * 部门编号
	 */
	private Integer departmentId;
	/**
	 * 消耗日期
	 */
	private Date consumeDate;
	/**
	 * 消耗金额
	 */
	private Double consumeMoney;
	/**
	 * 消耗类型 1.部门使用 2.部门调拨
	 */
	private Integer type = 1;
	/**
	 * 目标部门编号 如果为本部门消耗 则目标部门为本部门
	 */
	private Integer targetDepartId;
	/**
	 * 备注
	 */
	private String remarks;
	/**
	 * 临时编号
	 */
	private String tempId;
	/**
	 * 库存类型  1.领用  2.调拨
	 */
	private Integer stockType;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getOperatNo() {
		return operatNo;
	}

	public void setOperatNo(String operatNo) {
		this.operatNo = operatNo;
	}

	public Date getConsumeDate() {
		return consumeDate;
	}

	public void setConsumeDate(Date consumeDate) {
		this.consumeDate = consumeDate;
	}

	public Double getConsumeMoney() {
		return consumeMoney;
	}

	public void setConsumeMoney(Double consumeMoney) {
		this.consumeMoney = consumeMoney;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public String getTempId() {
		return tempId;
	}

	public void setTempId(String tempId) {
		this.tempId = tempId;
	}

	public Integer getTargetDepartId() {
		return targetDepartId;
	}

	public void setTargetDepartId(Integer targetDepartId) {
		this.targetDepartId = targetDepartId;
	}

	public Integer getDepartmentId() {
		return departmentId;
	}

	public void setDepartmentId(Integer departmentId) {
		this.departmentId = departmentId;
	}

	public Integer getStockType() {
		return stockType;
	}
	public void setStockType(Integer stockType) {
		this.stockType = stockType;
	}
}
