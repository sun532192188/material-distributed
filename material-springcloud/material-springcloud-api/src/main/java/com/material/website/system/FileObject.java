package com.material.website.system;

/**  
 * @Description: 传输到前台的fileObject对象 
 * @author 张明虎 zhangminghu@yuntengzhiyong.com  
 * @date 2014年12月18日 下午1:56:06  
 */

public class FileObject {
	private Integer fileId;
	/**
	 * 原名
	 */
	private String oldName;
	/**
	 * 新名
	 */
	private String newName;
	/**
	 * 大小
	 */
	private Long size;
	/**
	 * 保存文件夹
	 */
	private String savePath;
	/**
	 * 扩展名
	 */
	private String extension;
	/**
	 * 文件全路径
	 */
	private String fullPath;
	/**
	 * 保存类型 0为静态 1为动态
	 */
	private Integer saveType;
	/**
	 * @return the oldName
	 */
	public String getOldName() {
		return oldName;
	}
	/**
	 * @param oldName the oldName to set
	 */
	public void setOldName(String oldName) {
		this.oldName = oldName;
	}
	/**
	 * @return the newName
	 */
	public String getNewName() {
		return newName;
	}
	/**
	 * @param newName the newName to set
	 */
	public void setNewName(String newName) {
		this.newName = newName;
	}
	/**
	 * @return the size
	 */
	public Long getSize() {
		return size;
	}
	/**
	 * @param fize the size to set
	 */
	public void setSize(Long size) {
		this.size = size;
	}
	/**
	 * @return the savePath
	 */
	public String getSavePath() {
		return savePath;
	}
	/**
	 * @param savePath the savePath to set
	 */
	public void setSavePath(String savePath) {
		this.savePath = savePath;
	}
	/**
	 * @return the extension
	 */
	public String getExtension() {
		return extension;
	}
	/**
	 * @param extension the extension to set
	 */
	public void setExtension(String extension) {
		this.extension = extension;
	}
	/**
	 * @return the fullPath
	 */
	public String getFullPath() {
		return fullPath;
	}
	/**
	 * @param fullPath the fullPath to set
	 */
	public void setFullPath(String fullPath) {
		this.fullPath = fullPath;
	}
	/**
	 * @return the saveType
	 */
	public Integer getSaveType() {
		return saveType;
	}
	/**
	 * @param saveType the saveType to set
	 */
	public void setSaveType(Integer saveType) {
		this.saveType = saveType;
	}
	/**
	 * @return the fileId
	 */
	public Integer getFileId() {
		return fileId;
	}
	/**
	 * @param fileId the fileId to set
	 */
	public void setFileId(Integer fileId) {
		this.fileId = fileId;
	}
}
