package com.material.website.args;

import org.zh.validate.annotation.NotEmpty;
import org.zh.validate.annotation.NotNull;

/**
 * 入库参数类
 * 
 * @author sunxiaorong
 * 
 */
public class StorageAddArgs {

	/**
	 * 编号
	 */
	private Integer id;
	/**
	 * 入库编号
	 */
	@NotEmpty
	private String storageNo;
	/**
	 * 供应商编号
	 */
	@NotNull
	private Integer supplierId;
	/**
	 * 入库物资金额
	 */
	@NotNull
	private Double storageMoney;
	/**
	 * 入库类型 1.验收   2.预存
	 */
	@NotNull
	private Integer storageType;
	/**
	 * 签收部门
	 */
	@NotNull
	private Integer signDepart;
	/**
	 * 签收员姓名
	 */
	@NotEmpty
	private String signName;
	/**
	 * 备注
	 */
	private String remarks;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getStorageNo() {
		return storageNo;
	}
	public void setStorageNo(String storageNo) {
		this.storageNo = storageNo;
	}
	public Integer getSupplierId() {
		return supplierId;
	}
	public void setSupplierId(Integer supplierId) {
		this.supplierId = supplierId;
	}
	public Double getStorageMoney() {
		return storageMoney;
	}
	public void setStorageMoney(Double storageMoney) {
		this.storageMoney = storageMoney;
	}
	public Integer getStorageType() {
		return storageType;
	}
	public void setStorageType(Integer storageType) {
		this.storageType = storageType;
	}
	
	public Integer getSignDepart() {
		return signDepart;
	}
	public void setSignDepart(Integer signDepart) {
		this.signDepart = signDepart;
	}
	public String getSignName() {
		return signName;
	}
	public void setSignName(String signName) {
		this.signName = signName;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

}
